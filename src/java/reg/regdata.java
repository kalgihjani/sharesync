package reg;

/**
 *
 * @author DELL
 */
public class regdata {
    
    private String gdriveacc;
    private String gdrivefolder;
    private String accesscode;
    private String username;
    private String password;

    /**
     * @return the gdriveacc
     */
    public String getGdriveacc() {
        return gdriveacc;
    }

    /**
     * @param gdriveacc the gdriveacc to set
     */
    public void setGdriveacc(String gdriveacc) {
        this.gdriveacc = gdriveacc;
    }

    /**
     * @return the gdrivefolder
     */
    public String getGdrivefolder() {
        return gdrivefolder;
    }

    /**
     * @param gdrivefolder the gdrivefolder to set
     */
    public void setGdrivefolder(String gdrivefolder) {
        this.gdrivefolder = gdrivefolder;
    }

    /**
     * @return the accesscode
     */
    public String getAccesscode() {
        return accesscode;
    }

    /**
     * @param accesscode the accesscode to set
     */
    public void setAccesscode(String accesscode) {
        this.accesscode = accesscode;
    }

    /**
     * @return the username
     */
    public String getUsername() {
        return username;
    }

    /**
     * @param username the username to set
     */
    public void setUsername(String username) {
        this.username = username;
    }

    /**
     * @return the password
     */
    public String getPassword() {
        return password;
    }

    /**
     * @param password the password to set
     */
    public void setPassword(String password) {
        this.password = password;
    }
    
}

